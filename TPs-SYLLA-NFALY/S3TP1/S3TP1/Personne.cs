public abstract class Personne
{
	private string code;

	private string nom;

	private string prenom;


    public string Code
    {
        get { return code; }
        set { code = value; }
    }

    public string Nom
    {
        get { return nom; }
        set { nom = value; }
    }

    public string Prenom
    {
        get { return prenom; }
        set { prenom = value; }
    }

}

